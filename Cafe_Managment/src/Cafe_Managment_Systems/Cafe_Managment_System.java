    private void jBtnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnTotalActionPerformed
        if((!jlblCostDrinks.getText().equals("0")&&!jlblCostDrinks.getText().equals("0.00"))
                ||(!jlblCostCake.getText().equals("0")&& !jlblCostCake.getText().equals("0.00"))){
        double costOfDrink=Double.parseDouble(jlblCostDrinks.getText());
        double costOfCake=Double.parseDouble(jlblCostCake.getText());
        double costOfService=Double.parseDouble(jlblService.getText());
        double sum=costOfDrink+costOfCake+costOfService;
        double tax=3.9;
        double cTax=(sum*tax)/100;
        String ttax=String.format("%.2f", cTax);
        jlblTax.setText(ttax);
        String subSum=String.format("%.2f", sum);
        jlblSubTotal.setText(subSum);
        double totalSubTax=cTax+sum;
        String total=String.format("%.2f", totalSubTax);
        jlblTotal.setText(total);
        }
        
    }//GEN-LAST:event_jBtnTotalActionPerformed

