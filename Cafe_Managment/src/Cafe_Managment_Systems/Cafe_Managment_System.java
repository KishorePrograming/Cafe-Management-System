  private static boolean numCheck(JTextPane txt){
      String s = txt.getText();
      for(int i=0;i<s.length();i++){
          if('0'<=s.charAt(i)&&s.charAt(i)<='9'){
              
          }else{
              return false;
          }
      }
      return true;
  }
   private  void holderChangeOff(JTextPane txt){
      if(txt.getText().equals("")){
           txt.setText("0");
           txt.setForeground(blue);
       }
      else if(!txt.getText().equals("0")||!txt.getText().equals(""))
      {
          if(numCheck(txt)==true){
               jlblRemarks.setText("");
           }else{
              jlblRemarks.setText("Please enter numbers");
              txt.setText("0");
              txt.setForeground(blue);
          }
      }
  }
  
